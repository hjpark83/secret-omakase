/* ── Public API ── */

export function getRestaurants(): Restaurant[] {
  if (typeof window === "undefined") return SEED_DATA;
  const raw = localStorage.getItem(STORAGE_KEY);
  if (!raw) {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(SEED_DATA));
    return SEED_DATA;
  }
  return JSON.parse(raw) as Restaurant[];
}

export function saveRestaurants(list: Restaurant[]) {
  localStorage.setItem(STORAGE_KEY, JSON.stringify(list));
}

export function addRestaurant(r: Omit<Restaurant, "id">): Restaurant {
  const list = getRestaurants();
  const maxId = list.reduce((max, item) => Math.max(max, item.id), 0);
  const newItem: Restaurant = { ...r, id: maxId + 1 };
  list.push(newItem);
  saveRestaurants(list);
  return newItem;
}

export function updateRestaurant(id: number, updates: Partial<Restaurant>) {
  const list = getRestaurants();
  const idx = list.findIndex((r) => r.id === id);
  if (idx >= 0) {
    list[idx] = { ...list[idx], ...updates, id };
    saveRestaurants(list);
  }
}

export function deleteRestaurant(id: number) {
  const list = getRestaurants().filter((r) => r.id !== id);
  saveRestaurants(list);
}

export function getNextId(): number {
  const list = getRestaurants();
  return list.reduce((max, item) => Math.max(max, item.id), 0) + 1;
}

/** Geocode using Nominatim (free, no API key needed) */
export async function geocodeAddress(address: string): Promise<{ lat: number; lng: number } | null> {
  try {
    const res = await fetch(`https://nominatim.openstreetmap.org/search?format=json&q=${encodeURIComponent(address)}&limit=1`, {
      headers: { "Accept-Language": "ko" },
    });
    const data = await res.json();
    if (data.length > 0) {
      return { lat: parseFloat(data[0].lat), lng: parseFloat(data[0].lon) };
    }
    return null;
  } catch {
    return null;
  }
}
